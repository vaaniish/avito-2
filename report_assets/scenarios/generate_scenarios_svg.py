def esc(s: str) -> str:
    return html.escape(s, quote=True)


def wrap(text: str, max_chars: int):
    words = text.split()
    lines = []
    cur = ""
    for w in words:
        nxt = (cur + " " + w).strip()
        if len(nxt) <= max_chars:
            cur = nxt
        else:
            if cur:
                lines.append(cur)
            cur = w
    if cur:
        lines.append(cur)
    return lines


def step_block_height(step_text: str, max_chars=24):
    lines = wrap(step_text, max_chars)
    return max(66, 24 + 20 * len(lines)), lines


def title_block_height(title: str, max_chars=24):
    lines = wrap(title, max_chars)
    return max(78, 26 + 24 * len(lines)), lines


def build_group_svg(group_id: str, scenarios: list):
    # Unique color per scenario (S1..S16), no repeats.
    scenario_colors = {
        "S1": "#2F80ED",
        "S2": "#27AE60",
        "S3": "#F2994A",
        "S4": "#9B51E0",
        "S5": "#56CCF2",
        "S6": "#EB5757",
        "S7": "#219653",
        "S8": "#F2C94C",
        "S9": "#6FCF97",
        "S10": "#2D9CDB",
        "S11": "#BB6BD9",
        "S12": "#1ABC9C",
        "S13": "#D35400",
        "S14": "#16A085",
        "S15": "#8E44AD",
        "S16": "#C0392B",
    }

    margin = 22
    bottom_padding = 90
    col_w = 290
    col_gap = 16
    step_gap = 8

    # compute per-column heights
    col_heights = []
    title_infos = []
    step_infos = []
    for sc in scenarios:
        th, title_lines = title_block_height(f"{sc['id']}. {sc['name']}")
        sh_total = 0
        steps = []
        for step in sc["steps"]:
            h, lines = step_block_height(step)
            steps.append((h, lines))
            sh_total += h + step_gap
        if steps:
            sh_total -= step_gap
        total_h = th + 10 + sh_total
        col_heights.append(total_h)
        title_infos.append((th, title_lines))
        step_infos.append(steps)

    width = margin * 2 + len(scenarios) * col_w + (len(scenarios) - 1) * col_gap
    # Keep extra bottom space to avoid clipping in image viewers/renderers.
    height = margin * 2 + max(col_heights) + bottom_padding

    svg = []
    svg.append(f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}" viewBox="0 0 {width} {height}">')
    svg.append('<rect x="0" y="0" width="100%" height="100%" fill="#ffffff"/>')

    for i, sc in enumerate(scenarios):
        x = margin + i * (col_w + col_gap)
        y = margin
        color = scenario_colors.get(sc["id"], "#2F80ED")

        # scenario label (black, top)
        th, t_lines = title_infos[i]
        svg.append(f'<text x="{x + 4}" y="{y + 24}" font-size="19" font-weight="700" fill="#0f172a" font-family="Arial, DejaVu Sans, sans-serif">')
        for li, ln in enumerate(t_lines[:4]):
            dy = 0 if li == 0 else 23
            svg.append(f'<tspan x="{x + 4}" dy="{dy}">{esc(ln)}</tspan>')
        svg.append('</text>')

        sy = y + th + 10
        for si, (h, lines) in enumerate(step_infos[i], start=1):
            svg.append(f'<rect x="{x}" y="{sy}" width="{col_w}" height="{h}" rx="0" fill="{color}" stroke="#ffffff" stroke-width="1"/>')
            # step number + text
            svg.append(f'<text x="{x + 10}" y="{sy + 24}" font-size="16" font-weight="700" fill="#ffffff" font-family="Arial, DejaVu Sans, sans-serif">{si}.</text>')
            text_x = x + 38
            text_y = sy + 24
            svg.append(f'<text x="{text_x}" y="{text_y}" font-size="17" fill="#ffffff" font-family="Arial, DejaVu Sans, sans-serif">')
            for li, ln in enumerate(lines[:5]):
                dy = 0 if li == 0 else 20
                svg.append(f'<tspan x="{text_x}" dy="{dy}">{esc(ln)}</tspan>')
            svg.append('</text>')
            sy += h + step_gap

    svg.append('</svg>')

    out_svg = OUT / f"{group_id}.svg"
    out_svg.write_text("\n".join(svg), encoding="utf-8")
    return out_svg


