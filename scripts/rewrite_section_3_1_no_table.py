def main() -> None:
    if not DOC_PATH.exists():
        raise FileNotFoundError(f"Не найден файл: {DOC_PATH}")

    doc = docx.Document(str(DOC_PATH))

    # 3.1 и 3.2 определяем как два последних заголовка 2-го уровня перед "Заключением".
    h1 = [i for i, p in enumerate(doc.paragraphs) if p.style and p.style.name == "1"]
    if len(h1) < 2:
        raise RuntimeError("Не найдены разделы уровня 1")
    idx_conclusion = h1[-1]

    h2 = [
        i
        for i, p in enumerate(doc.paragraphs[:idx_conclusion])
        if p.style and p.style.name == "2"
    ]
    if len(h2) < 2:
        raise RuntimeError("Не найдены подразделы уровня 2")
    idx_31 = h2[-2]
    idx_32 = h2[-1]
    if idx_32 <= idx_31:
        raise RuntimeError("Некорректный порядок 3.1 и 3.2")

    p31 = doc.paragraphs[idx_31]._p
    p32 = doc.paragraphs[idx_32]._p

    # Удаляем все элементы (и абзацы, и таблицы) между 3.1 и 3.2.
    el = p31.getnext()
    while el is not None and el is not p32:
        nxt = el.getnext()
        el.getparent().remove(el)
        el = nxt

    body_style = None
    for j in range(idx_31 - 1, -1, -1):
        p = doc.paragraphs[j]
        if p.text.strip() and p.style and p.style.name not in {"1", "2", "3"}:
            body_style = p.style
            break
    if body_style is None:
        body_style = doc.styles["Normal"]

    anchor = doc.paragraphs[idx_31 + 1]
    for text in PARAGRAPHS_3_1:
        p = anchor.insert_paragraph_before(text)
        p.style = body_style

    doc.save(str(OUT_PATH))
    print(f"OK: 3.1 rewritten without table -> {OUT_PATH}")


if __name__ == "__main__":
    main()
